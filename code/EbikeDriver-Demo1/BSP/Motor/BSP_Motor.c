TIM_HandleTypeDef htim1;
ADC_HandleTypeDef hadc2;

/**
 * 电机初始化
 */
void BSP_Motor_Init(void)
{
  MX_TIM1_Init();
  MX_ADC2_Init();
}
//启动电机
void BSP_Motor_Start(void)
{
  /* ADC校准（初始化后、启动前执行） */
  HAL_ADCEx_Calibration_Start(&hadc2, ADC_SINGLE_ENDED);
  HAL_Delay(500);

  HAL_ADCEx_InjectedStart_IT(&hadc2);
  HAL_TIM_Base_Start(&htim1);

  // 启动PWM输出）
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);

  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_2);
  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_3);

  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_4);
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 4000);
}
//停止电机
void BSP_Motor_Stop(void)
{
  /* 停止TIM1时间基 */
  HAL_TIM_Base_Stop(&htim1);

  // 停止 PWM 和计数
  HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);
  HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_2);
  HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_3);
  HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_1);
  HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_2);
  HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_3);

  HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_4);

}
//返回ADC采集原始值
ADC_RawValue_t BSP_Motor_GetAdcRawValue(void)
{
  ADC_RawValue_t ADC_RawValue = {0};

  ADC_RawValue.Ua = HAL_ADCEx_InjectedGetValue(&hadc2,ADC_INJECTED_RANK_3);
  ADC_RawValue.Ub = HAL_ADCEx_InjectedGetValue(&hadc2,ADC_INJECTED_RANK_2);
  ADC_RawValue.Uc = HAL_ADCEx_InjectedGetValue(&hadc2,ADC_INJECTED_RANK_1);
  ADC_RawValue.Ubus= HAL_ADCEx_InjectedGetValue(&hadc2,ADC_INJECTED_RANK_4);

  return ADC_RawValue;
}

